    <h1>КОНЦЕПТУАЛЬНОСТЬ</h1>
    <img src="img/konc1.jpg" alt="1">
    <p><strong>Успех любого коммерческого заведения</strong> в современном  мире зависит от концептуальности, когда в одно заведения люди ходят, а в другое нет... Почему? Сейчас достаточно трудно выделится  кухней или сервисом. В ХХI веке эти опции идут по умолчанию. Что же лежит в основе успеха? А в основе успеха лежит концепт-основная идея проекта, замысел, и форма реализации  этой идеи, все это взаимосвязано друг с другом и формирует дизайн решение.</p>
     <img src="img/konc2.jpg" alt="1">
    <p><strong>На сегодняшний день информации довольно много</strong> и человеку сложно считывать её и замечать особенности. При создании концептуального заведения выделяется основная суть проекта и передаётся  основной смысл  средствами дизайна и маркетинга. </p>
  
  <img src="img/konc3.jpg" alt="1">
    <p><strong>Дизайн в этом случае играет ключевую роль</strong> и имеет  особое влияние на будущих клиентов или гостей. Создание тематического заведения погружает человека в атмосферу и дает возможность попасть не просто в красивое место, но и почувствовать атмосферу, которая оставляет особое впечатление и позволяет уйти от обыденности, стандартов и суеты.</p>
